const mongoose = require('mongoose');

const VenueSchema = mongoose.Schema({
  imgPath: {
    type: String,
    required: true,
  },
  location: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model('Venue', VenueSchema);
